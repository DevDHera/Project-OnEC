     * Store a newly created resource in storage.
     *
     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\Response
     */
    public function store(Request $request)
    {
        $stures = new StudentResults;
        $stures->user_id =$request->input('uid');
        $stures->exampprid =$request->input('eid');
        $stures->module =ExamPaper::find($request->input('eid'))->module;
        $stures->result =$request->input('result');
        $stures->save();

        return redirect('/home');
    }

